# 画一个边长为200的五角星

import turtle

turtle.begin_fill()
turtle.fillcolor('red')
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.left(72)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.left(72)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.left(72)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.left(72)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.end_fill()
turtle.hideturtle()
turtle.done()